package lesson200206;

public class Increment {

    public static void main(String []  args) {

     int i=3;
     int j=3;

        System.out.println(i++); // out: 3 -> потом происходит ++
        System.out.println(++j); // out: 4

        System.out.println(i); // out: 4
        System.out.println(j); // out: 4


    }


}
